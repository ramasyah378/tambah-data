<p align="justify"><span class="style5">Apa Itu PHP?</span><br />
</p>
<p align="justify">&nbsp;</p>
<p align="justify"><img src="Image/Logo PHP[5].png" width="251" height="133" hspace="20" border="0" align="left" />PHP adalah script untuk pemrograman script web server-side, script yang membuat dokumen HTML secara&nbsp;<em>on the fly</em>, dokumen HTML yang dihasilkan dari suatu aplikasi bukan dokumen HTML yang dibuat dengan menggunakan editor teks atau editor HTML.</p>
<p align="justify">Dengan menggunakan PHP maka maintenance suatu situs web menjadi lebih mudah. Proses update data dapat dilakukan dengan menggunakan aplikasi yang dibuat dengan menggunakan script PHP.</p>
<p align="justify">PHP/FI merupakan nama awal dari PHP. PHP merupakan singkatan dari Personal Home Page dan FI adalah singkatan dari Form Interface. Dibuat pertama kali oleh Rasmus Lerdoff. PHP, awalnya merupakan program CGI yang dikhususkan untuk menerima input melalui form yang ditampilkan dalam browser web.</p>
<p align="justify">Software ini disebarkan dan dilisensikan sebagai perangkat lunak Open Source.</p>
<p align="justify">PHP secara resmi merupakan kependekan dari&nbsp;<em>PHP: HyperText Preprocessor</em>, merupakan bahasa script server-side yang disisipkan pada HTML.</p>
<p align="justify">Berikut ini adalah contoh yang umum digunakan untuk menjelaskan tentang PHP sebagai script yang disisipkan (embedded script) dalam dokumen HTML.</p>
<blockquote class="style6"> &lt;html&gt;<br />
&lt;head&gt;<br />  &lt;title&gt;Contoh&lt;/title&gt;<br />
&lt;/head&gt;<br />
&lt;body&gt;&lt;?phpecho &ldquo;Hai, saya dari script PHP!&rdquo;;?&gt;<br />
&lt;/body&gt;<br />
&lt;/html&gt;</blockquote>
<p align="justify">Contoh script PHP di atas berbeda dengan script yang dituliskan dengan bahasa lain seperti C atau Perl. Pemrograman tidak harus menuliskan semua dokumen HTML sebagai bagian dari keluaran dari script PHP, cukup menuliskan bagian mana saja yang berupa tag HTML dan bagian mana yang harus ditulis atau dihasilkan dari program script PHP. Kode/script PHP diapit dengan menggunakan tag awal dan tag akhir yang khusus, yang memungkinkan pemrograman untuk masuk dan keluar dari mode script PHP.</p>
<h3 align="justify" class="style6">Kemampuan PHP</h3>
