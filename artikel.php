<h3 align="justify" class="style6">Sejarah PHP</h3>
<p align="justify">PHP pertama kali dibuat pada musim gugur tahun 1994 oleh Rasmus Lerdoff, awalnya digunakan pada websitenya untuk mencatat siapa saja yang berkunjung dan melihat biodataya. Versi pertama yang dirilis tersedia pada awal tahun 1995, dikenal sebagai tool Personal Home Page, yang terdiri atas engine parser yang sangat sederhana yang hanya mengerti beberapa makro khusus dan sejumlah utilitas yang sering digunakan pada halaman-halaman web, seperti buku tami, counter pengunjung, dan lainnya. Parser diprogram ulang pada pertengahan 1995 dan diberi nama PHP/FI versi 2.0. FI berasal dari paket Rasmus lainnya yang ditulis untuk menginterpretasi data dari form, yang kemudian dikombinasikan dengan tool Personal Home Page dan ditambahkan dukungan untuk database mSQL (mini SQL).</p>
<p align="justify">Tahun 1995 ini dianggap sebagai tahun kelahiran dari PHP/FI yang kemudian membuat pertumbuhan aplikasi web yang pesat, dan banyak orang kemudian berkontribusi mengembangkan PHP/FI. Sulit untuk mendapatkan statistik yang tepat untuk memperkirakan panggunaan PHP/FI, tetapi diperkirakan pada akhir 1996 telah digunakan oleh sedikitnya 15000 website di seluruh dunia. Dan pertengahan 1997 mencapai 50000 situs.</p>
<p align="justify">Pada pertengahan 1997 ini juga terjadi perubahan pengembangan PHP. Pengembangan dilakukan oleh tim yang terorganisasi bukan oleh Rasmus sendiri saja lagi. Parser dikembangkan oleh Zeev Suraski dan Andi Gutmans yang kemudian menjadi dasar untuk versi 3, dan banyak utilitas tambahan yang diprogram untuk menambah kemampuan dari versi 2. Versi terakhir (PHP 4) menggunakan engine script Zend untuk lebih meningkatkan kinerja (performance) dan mempunyai dukungan yang banyak berupa ekstensi dan fungsi dari berbagai library pihak ketiga (third party), dan berjalan seolah modul asli (native) dari berbagai server web yang populer.</p>
<p align="justify">Sejak Januari 2001 PHP3 dan PHP4 disertakan pada sejumlah produk server web komersial seperti web Stronghold RedHat. Perkiraan konservatif yang didapat dari angka yang diberikan oleh Netcraft (<a href="http://www.netcraft.com/">http://www.netcraft.com</a>) yang diekstrapolasi, pengguna PHP sekitar 5.100.000 sedikit lebih banyak dari server web yang menggunakan Microsoft IIS (5,03 juta) di internet.</p>
