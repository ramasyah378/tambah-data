<p align="justify">Kemampuan (fitur) PHP yang paling diandalkan dan signifikan adalah dukungan kepada banyak database. Membuat halaman web yang menggunakan data dari database dengan sangat mudah dapat dilakukan. Berikut adalah daftar database yang didukung oleh PHP:</p>
